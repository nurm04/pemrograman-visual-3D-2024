    private void submitTransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitTransaksiActionPerformed
        String peran = PeranFormTransaksi.getText();
//        addTransaksi
        if (titleFormTransaksi.getText().equalsIgnoreCase("Tambah Data Transaksi")) {    
            if (DataKaryawanFormTransaksi.getSelectedIndex() == 0) {
                errDataKaryawan.setText("Anda belum memilih data karyawan");
            } else {
                errDataKaryawan.setText(" ");
            }
            if (DataProyekFormTransaksi.getSelectedIndex() == 0) {
                errDataProyek.setText("Anda belum memilihdata proyek");
            } else {
                errDataProyek.setText(" ");
            }
            if (peran.trim().isEmpty()) {
                errPeran.setText("Form Peran tidak boleh kosong");
            } else {
                errPeran.setText(" ");
            }

            if (errDataKaryawan.getText().equals(" ") && 
                errDataProyek.getText().equals(" ") &&
                errPeran.getText().equals(" ")
            ) {
                String data_karyawan = DataKaryawanFormTransaksi.getSelectedItem().toString();
                String[] ambilIdKaryawan = data_karyawan.split("\\|");
                int id_karyawan = Integer.parseInt(ambilIdKaryawan[0].trim());
                
                String data_proyek = DataProyekFormTransaksi.getSelectedItem().toString();
                String[] ambilIdProyek = data_proyek.split("\\|");
                int id_proyek = Integer.parseInt(ambilIdProyek[0].trim());

                boolean hasil = Transaksi.addTransaksi(id_karyawan, id_proyek, peran);
                if (hasil) {
                    FormTransaksiFrame.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Berhasil menambahkan Data Transaksi", "Alert", JOptionPane.INFORMATION_MESSAGE);
                    listTransaksi();
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal menambahkan Data Transaksi", "Alert", JOptionPane.ERROR_MESSAGE);
                }
            }
        } 
//        updateTransaksi
        else {
            int id_transaksi = Integer.parseInt(idFormTransaksi.getText());
            if (peran.trim().isEmpty()) {
                errPeran.setText("Form Peran tidak boleh kosong");
            } else {
                errPeran.setText(" ");
            }

            if (errDataKaryawan.getText().equals(" ") && 
                errDataProyek.getText().equals(" ") &&
                errPeran.getText().equals(" ")
            ) {
                String data_karyawan = DataKaryawanFormTransaksi.getSelectedItem().toString();
                String[] ambilIdKaryawan = data_karyawan.split("\\|");
                int id_karyawan = Integer.parseInt(ambilIdKaryawan[0].trim());
                
                String data_proyek = DataProyekFormTransaksi.getSelectedItem().toString();
                String[] ambilIdProyek = data_proyek.split("\\|");
                int id_proyek = Integer.parseInt(ambilIdProyek[0].trim());
                
                boolean hasil = Transaksi.updateTransaksi(id_transaksi, id_karyawan, id_proyek, peran);
                if (hasil) {
                    FormTransaksiFrame.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Berhasil memperbarui Data Transaksi", "Alert", JOptionPane.INFORMATION_MESSAGE);
                    listTransaksi();
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal memperbarui Data Transaksi", "Alert", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_submitTransaksiActionPerformed
