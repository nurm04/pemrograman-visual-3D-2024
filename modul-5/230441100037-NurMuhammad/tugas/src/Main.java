    private void resetFormTransaksi() {
        errDataKaryawan.setText(" ");
        errDataProyek.setText(" ");
        titleFormProyek.setText("");
        idFormProyek.setText(" ");
    }
    private void listTransaksi() {
        List<Transaksi> transaksiList = Transaksi.getAllTransaksi();
        DefaultTableModel model = (DefaultTableModel) tbl_Transaksi.getModel();
        model.setRowCount(0);
        
        for (Transaksi transaksi : transaksiList) {  
            Karyawan karyawan = Karyawan.getKaryawan(transaksi.getId_karyawan());
            Proyek proyek = Proyek.getProyek(transaksi.getId_proyek());
            model.addRow(new Object[]{
                transaksi.getId_transaksi(),
                karyawan.getNama(),
                proyek.getNama_proyek(),
                transaksi.getPeran(),
                "Edit",
                "Hapus"
            });
        }
    }
