    private void submitKaryawanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitKaryawanActionPerformed
        String nama = namaKaryawan.getText();
        String jabatan = jabatanKaryawan.getText();
        String departemen = departemenKaryawan.getText();
//        addKaryawan
        if (titleFormKaryawan.getText().equalsIgnoreCase("Tambah Data Karyawan")) {    
            if (nama.trim().isEmpty()) {
                errNamaKaryawan.setText("Form Nama tidak boleh kosong");
            } else {
                errNamaKaryawan.setText(" ");
            }
            if (jabatan.trim().isEmpty()) {
                errJabatanKaryawan.setText("Form Jabatan tidak boleh kosong");
            } else {
                errJabatanKaryawan.setText(" ");
            }
            if (departemen.trim().isEmpty()) {
                errDepartemenKaryawan.setText("Form Departemen tidak boleh kosong");
            } else {
                errDepartemenKaryawan.setText(" ");
            }

            if (errNamaKaryawan.getText().equals(" ") && 
                errJabatanKaryawan.getText().equals(" ") && 
                errDepartemenKaryawan.getText().equals(" ")
            ) {
                boolean hasil = Karyawan.addKaryawan(nama, jabatan, departemen);
                if (hasil) {
                    FormKaryawanFrame.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Berhasil menambahkan Data Karyawan", "Alert", JOptionPane.INFORMATION_MESSAGE);
                    listKaryawan();
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal menambahkan Data Karyawan", "Alert", JOptionPane.ERROR_MESSAGE);
                }
            }
        } 
//        updateKaryawan
        else {
            int id = Integer.parseInt(idFormKaryawan.getText());
            if (nama.trim().isEmpty()) {
                errNamaKaryawan.setText("Form Nama tidak boleh kosong");
            } else {
                errNamaKaryawan.setText(" ");
            }
            if (jabatan.trim().isEmpty()) {
                errJabatanKaryawan.setText("Form Jabatan tidak boleh kosong");
            } else {
                errJabatanKaryawan.setText(" ");
            }
            if (departemen.trim().isEmpty()) {
                errDepartemenKaryawan.setText("Form Departemen tidak boleh kosong");
            } else {
                errDepartemenKaryawan.setText(" ");
            }

            if (errNamaKaryawan.getText().equals(" ") && 
                errJabatanKaryawan.getText().equals(" ") && 
                errDepartemenKaryawan.getText().equals(" ")
            ) {
                boolean hasil = Karyawan.updateKaryawan(id, nama, jabatan, departemen);
                if (hasil) {
                    FormKaryawanFrame.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Berhasil memperbarui Data Karyawan", "Alert", JOptionPane.INFORMATION_MESSAGE);
                    listKaryawan();
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal memperbarui Data Karyawan", "Alert", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_submitKaryawanActionPerformed
    private void tbl_KaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_KaryawanMouseClicked
        int row = tbl_Karyawan.rowAtPoint(evt.getPoint());
        int column = tbl_Karyawan.columnAtPoint(evt.getPoint());
        
        if (column == 4) {
            int id = (int) tbl_Karyawan.getValueAt(row, 0);
            String nama = (String) tbl_Karyawan.getValueAt(row, 1);
            String jabatan = (String) tbl_Karyawan.getValueAt(row, 2);
            String departemen = (String) tbl_Karyawan.getValueAt(row, 3);
            
            resetFormKaryawan();
            FormKaryawanFrame.setVisible(true);
            titleFormKaryawan.setText("Edit Data Karyawan ID: ");
            idFormKaryawan.setText(Integer.toString(id));
            
            namaKaryawan.setText(nama);
            jabatanKaryawan.setText(jabatan);
            departemenKaryawan.setText(departemen);
        }
        if (column == 5) {
            int id = (int) tbl_Karyawan.getValueAt(row, 0);
            
            int pilihan = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus data id " + id +" ?", "peringatan", JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                boolean hasil = Karyawan.deleteKaryawan(id);
                if (hasil) {
                    JOptionPane.showMessageDialog(null, "Berhasil menghapus Data Karyawan", "Alert", JOptionPane.INFORMATION_MESSAGE);
                    listKaryawan();
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal menghapus Data Karyawan", "Alert", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_tbl_KaryawanMouseClicked
